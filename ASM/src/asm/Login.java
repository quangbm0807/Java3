    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String enteredUsername = txtUsername.getText().trim();
        char[] enteredPasswordChars = txtPassword.getPassword();
        String enteredPassword = new String(enteredPasswordChars);

        if (enteredUsername.isEmpty() || enteredPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username and password cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            java.sql.Connection conn = DriverManager.getConnection(url, user, password);
            String query = "SELECT * FROM users WHERE username = ? AND password = ?";
            try (PreparedStatement preparedStatement = conn.prepareStatement(query)) {
                preparedStatement.setString(1, enteredUsername);
                preparedStatement.setString(2, enteredPassword);

                try (ResultSet resultSet = preparedStatement.executeQuery()) {
                    if (resultSet.next()) {
                        String username = resultSet.getString("username");
                        String password = resultSet.getString("password");
                        boolean role = (resultSet.getInt("role") == 0);
                        if (role) {
                            QuanLySinhVien loginFrame = new QuanLySinhVien(new javax.swing.JFrame(), true);

                            this.dispose();

                            loginFrame.setVisible(true);

                        } else {
                            QuanLyDiem loginFrame = new QuanLyDiem(new javax.swing.JFrame(), true);

                            this.dispose();

                            loginFrame.setVisible(true);
                        }
                        User loggedInUser = new User(username, password, role);

                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Sai tên đăng nhập hoặc mật khẩu", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
                if (chkRemember.isSelected()) {
                    savePreferences();
                }
            }
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error connecting to the database", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

