    private void loadPreferences() {
        Preferences prefs = Preferences.userNodeForPackage(Login.class);
        String savedUsername = prefs.get(PREF_USERNAME, "");
        String savedPassword = prefs.get(PREF_PASSWORD, "");
        boolean remember = prefs.getBoolean(PREF_REMEMBER, false);
        txtUsername.setText(savedUsername);
        txtPassword.setText(savedPassword);
        chkRemember.setSelected(remember);
    }

    private void savePreferences() {
        Preferences prefs = Preferences.userNodeForPackage(Login.class);
        prefs.put(PREF_USERNAME, txtUsername.getText());
        prefs.put(PREF_PASSWORD, new String(txtPassword.getPassword()));
        prefs.putBoolean(PREF_REMEMBER, chkRemember.isSelected());
    }

